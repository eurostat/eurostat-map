function generateDifferentObject(originalObject) {
    const result = {}

    Object.keys(originalObject).forEach((key) => {
        let newValue
        // Keep generating new values until it's different from the original
        do {
            newValue = Math.random() < 0.5 ? 0 : 1
        } while (newValue === originalObject[key])

        if (newValue !== 0) {
            result[key] = newValue
        }
    })

    return result
}
